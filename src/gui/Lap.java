    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Lap lapObj = new LapBuilder().setBrand(brand).setModel(Model).setRam(ram).setGpu(gpu).setProcessor(processor).
                setHardDrive(hardDrive).setScreenSize(screenSize).setWifi(wifi).setBluetooth(bluetooth).setOs(os).getLap();

        System.out.println(lapObj);

        if (brand != null) {

            brand1.setText(brand);

        } else {
            brand1.setText("- - - - - - - - - - - -");
        }

        if (Model != null) {

            model1.setText(Model);

        } else {
            model1.setText("- - - - - - - - - - - -");
        }

        if (ram != 0) {

            ram1.setText(ram + " GB");

        } else {
            ram1.setText("- - - - - - - - - - - -");
        }

        if (gpu != 0) {

            gpu1.setText(gpu + " GB");

        } else {
            gpu1.setText("- - - - - - - - - - - -");
        }

        if (processor != null) {

            procesor1.setText(processor);

        } else {
            procesor1.setText("- - - - - - - - - - - -");
        }

        if (hardDrive != 0) {

            hard1.setText(hardDrive + " GB");

        } else {
            hard1.setText("- - - - - - - - - - - -");
        }

        if (screenSize != 0) {

            screen1.setText(screenSize + " inches");

        } else {
            screen1.setText("- - - - - - - - - - - -");
        }

        if (jRadioButton22.isSelected()) {
            wifi1.setText("");
        } else {
            wifi1.setText("");
        }
        if (jRadioButton25.isSelected()) {
            bluetooth1.setText("");
        } else {
            bluetooth1.setText("");
        }

        if (os != null) {

            os1.setText(os);

        } else if (jRadioButtonMac.isSelected()) {
            os1.setText("Mac OS");

        } else {
            os1.setText("- - - - - - - - - - - -");
        }

        jPanel3.setVisible(true);

    }//GEN-LAST:event_jButton1ActionPerformed

